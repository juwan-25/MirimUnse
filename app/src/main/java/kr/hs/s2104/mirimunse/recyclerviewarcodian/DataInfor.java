package kr.hs.s2104.mirimunse.recyclerviewarcodian;

public class DataInfor {
    String title;
    String context;

    public DataInfor(String title, String context){
        this.title = title;
        this.context = context;
    }

    public String getTitle() {
        return title;
    }

    public String getcontext() {
        return context;
    }
}